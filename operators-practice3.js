/*Arithmetic Operators
+: additional
-: subtractional
*: multiplicatonal
/: divisional
%: module
**: Expontiation(power)

unary operators are:
increment: ++
decrement: --
a++ : post increment
++a : pre increment
b++ : post decrement
++b : pre decrement
ex: a+b here a+b are expression , + is operator and a,b are the operands.
*/
let a = 5;
let b = 3;
console.log("a+b = ",a+b);
console.log("a-b = ",a-b);
console.log("a*b = ",a*b);
console.log("a/b = ",a/b);
console.log("a%b = ",a%b);
console.log("a**b = ",a**b);
console.log("a = ",a,"b = ",b);
a++; // first print then increment
b--;
console.log("a = ",a);
console.log("b = ",b);
console.log("++a = ",++a); // first increment then print

/*
Assignment Operator 
= : equal to
+= : add equal to
-= : sub equal to
*= : multiply equal to
%= : mod equal to
*/
 
a **= 4; 
console.log("a = ",a);
 

